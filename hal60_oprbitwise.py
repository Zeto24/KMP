# & atau and= Bitwise and
# | atau or= Bitwise or
# ^ = Exclusive or
# ~ = Bitwise not
# >> = Bitwise shift right
# << = Bitwise shift left

a = 10
b = 8

print(f'{a} & {b}  = {a & b}')
print(f'{a} | {b}  = {a | b}')
print(f'{a} ^ {b}  = {a ^ b}')
print(f'  ~ {a}  = {~ a}')
print(f'   ~ {b}  = {~ b}')
print(f'{b} >> 1  = {b >> 1}')
print(f'{b} << 1  = {b << 1}')