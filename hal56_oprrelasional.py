# == : sama dengan
# != : tidak sama dengan
# >  : lebih besar
# >= : lebih besar atau sama dengan
# <  : lebih kecil
# <= : lebih kecil atau sama dengan

a = 10
b = 8

print(f'{a} == {b} : {a == b}')
print(f'{a} != {b} : {a != b}')
print(f'{a} > {b}  : {a > b}')
print(f'{a} >= {b} : {a >= b}')
print(f'{a} < {b}  : {a < b}')
print(f'{a} <= {b} : {a <= b}')