# +  = Penjumlahan
# -  = Pengurangan
# *  = Perkalian
# /  = Pembagian bilangan riil
# // = Pembagian bilangan bulat
# %  = Sisa bagi
# ** = Perpangkatan

a = 10
b = 3

print(f'{a} + {b}  = {a + b}')
print(f'{a} - {b}  = {a - b}')
print(f'{a} * {b}  = {a * b}')
print(f'{a} / {b}  = {a / b}')
print(f'{a} // {b} = {a // b}')
print(f'{a} % {b}  = {a % b}')
print(f'{a} ** {b} = {a ** b}')